#1.Creating a Comment

#Comments starts with a #, and Python will ignore them:

#Example 1
#This is a comment
print("Hello, Professor!")

#Example 2
print("Hello, Professor!") #This is a comment

#2.Multiline Comments

#Example 1
#This is a comment
#written in
#more than just one line
print("Hello, World!")

#You can add a multiline string (triple quotes) in your code

#Example 2
"""
This is a comment
written in
more than just one line
"""
print("Hello, World!")